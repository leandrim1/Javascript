
// Função que realiza a soma de dois números
function soma(item1, item2) {
    // Retorna a soma de item1 e item2
    return item1 + item2;
}

// Função que realiza a subtração entre dois números
function diminuir(item1, item2) {
    // Retorna a diferença entre item1 e item2
    return item1 - item2;
}

// Chama a função soma com 5 e 6, e imprime o resultado no console
console.log('SOMA:', soma(5, 6)); // Espera-se imprimir: SOMA: 11

// Chama a função diminuir com 8 e 6, e imprime o resultado no console
console.log('Diminuir:', diminuir(8, 6)); // Espera-se imprimir: Diminuir: 2
