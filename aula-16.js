let listaCompras = ['leite', 'maçã', 'iogurte'];

// Executa toda a listaCompras
console.log(listaCompras);
// Adicionou o 'arroz' na listaCompras
console.log(listaCompras.push('arroz'));
// Removeu o ultimo item adicionado 'arroz'
console.log(listaCompras.pop());
// Removu o primeiro item da listaCompras 'leite'
console.log(listaCompras.shift());