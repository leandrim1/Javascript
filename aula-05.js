// Inicializa a variável 'idade' com o valor 1
let idade = 1;

// Inicia um loop 'while' que continuará enquanto 'idade' for menor ou igual a 18
while (idade <= 18) {
    // Imprime 'NAO PASSOU' seguido do valor atual de 'idade'
    console.log('NAO PASSOU', idade);
    // Incrementa 'idade' em 1
    idade = idade + 1;
}
